/* USER CODE BEGIN 0 */
float map(float value, float low1, float high1, float low2, float high2) {
	return low2 + (value - low1) * (high2 - low2) / (high1 - low1);
}

// higher base frequency results in higher lowest pitch
float pitchBaseFrequency = 1.0953e6;
uint32_t lastPitchMeasurementCPUClockCount = 0;
uint32_t pitchPulsesElapsed = 0;

float volumeBaseFrequency = 1.0953e6;
uint32_t lastVolumeMeasurementCPUClockCount = 0;
uint32_t volumePulsesElapsed = 0;

extern "C" void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) {
	if (htim->Instance == TIM1) {
		pitchBaseFrequency = htim4.Init.Period
				* (SystemCoreClock / static_cast<float>(pitchPulsesElapsed)) + 100;
		volumeBaseFrequency = htim8.Init.Period
				* (SystemCoreClock / static_cast<float>(volumePulsesElapsed)) + 100;
	}
}

void pitchMeasurementInterruptHandler(void) {
	uint32_t currentCPUClockCount = DWT->CYCCNT;
	pitchPulsesElapsed = currentCPUClockCount
			- lastPitchMeasurementCPUClockCount;

	float frequency = htim4.Init.Period
			* (SystemCoreClock / static_cast<float>(pitchPulsesElapsed));

	const float readoutMin = 0;
	const float readoutMax = 7000;

	const float targetMin = 50;
	const float targetMax = 1046.5;

	frequency -= pitchBaseFrequency;
	frequency *= -1;

	if (frequency < readoutMin)
		frequency = readoutMin;
	if (frequency > readoutMax)
		frequency = readoutMax;

	frequency = map(frequency, readoutMin, readoutMax, targetMin, targetMax);
	if (frequency == targetMin)
		frequency = 0;

	theremin::updatePitch(frequency);

	lastPitchMeasurementCPUClockCount = currentCPUClockCount;
	HAL_TIM_IRQHandler(&htim4);
}

void volumeMeasurementInterruptHandler(void) {
	uint32_t currentCPUClockCount = DWT->CYCCNT;
	volumePulsesElapsed = currentCPUClockCount
			- lastVolumeMeasurementCPUClockCount;

	float frequency = htim8.Init.Period
			* (SystemCoreClock / static_cast<float>(volumePulsesElapsed));

	const float fMax = 1000;
	frequency -= volumeBaseFrequency - fMax;

	if (frequency < 0)
		frequency = 0;
	if (frequency > fMax)
		frequency = fMax;

	float volume = frequency / fMax;

	theremin::updateVolume(volume);

	lastVolumeMeasurementCPUClockCount = currentCPUClockCount;
	HAL_TIM_IRQHandler(&htim8);
}

void dmaTransferHalf(DAC_HandleTypeDef*) {
	for (int i = 0; i < AUDIO_BUFFER_SIZE; ++i) {
		AudioBuffer[i] = 2048.f * theremin::processSample() + 2047.f;
	}
}

void dmaTransferComplete(DAC_HandleTypeDef*) {
	for (int i = AUDIO_BUFFER_SIZE; i < 2 * AUDIO_BUFFER_SIZE; ++i) {
		AudioBuffer[i] = 2048.f * theremin::processSample() + 2047.f;
	}
}
